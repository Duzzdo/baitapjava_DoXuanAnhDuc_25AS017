package Bai4.b4_3;

public class Point2D {
    protected float x;
    protected float y;

    public Point2D() {
        this(0, 0);
    }

    public Point2D(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float[] getXY() {
        return new float[]{x, y};
    }

    @Override
    public String toString() {
        return "(" + x + "," + y + ")";
    }
}
